#include "libft.h"

int	ft_isalnum(int ch)
{
	if (((64 < ch) && (ch < 91)) \
			|| ((96 < ch) && (ch < 123)) \
			|| ((47 < ch) && (ch < 58)))
	{
		return (1);
	}
	return (0);
}
